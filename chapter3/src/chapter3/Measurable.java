package chapter3;

/**
 * @author stevelyons
 * An interface for methods that return
 * the perimeter and area of an object.
 */
public interface Measurable 
{
	/** Task: Gets the perimeter.
	 * @return the perimeter */
	public double getPerimeter();
	/** Task: Gets the area.
	 * @return the area */
	public double getArea();
} // end Measurable
